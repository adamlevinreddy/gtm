// Slack message
// ----------------------------------------------------------------------------

function buildCrmMessage(p: CrmProposal, recUrl: string, logged: boolean): { text: string; blocks: object[] } {
  const text = `CRM updates suggested for ${p.companyName} — ${p.dealName}`;
  const blocks: object[] = [
    { type: "header", text: { type: "plain_text", text: "🗂️  CRM updates", emoji: true } },
    {
      type: "context",
      elements: [{ type: "mrkdwn", text: `*${p.companyName}* · deal *${p.dealName}* · HubSpot is the system of record` }],
    },
    {
      type: "section",
      text: {
        type: "mrkdwn",
        text:
          `${logged ? "✅" : "⚠️"} *Logged to the deal* (auto): meeting summary + recording link${logged ? "" : " — _logging failed, see logs_"}\n` +
          `▶ <${recUrl}|Watch recording & read transcript>`,
      },
    },
  ];

  const suggestionLines: string[] = [];
  if (p.suggestedStageLabel) {
    suggestionLines.push(
      `• *Stage:* ${p.currentStageLabel ?? "current"} → *${p.suggestedStageLabel}*${p.stageReason ? ` — ${p.stageReason}` : ""}`
    );
  }
  for (const f of p.fieldSuggestions) {
    suggestionLines.push(`• *${f.object} · ${f.property}:* ${f.currentValue ?? "—"} → *${f.suggestedValue}*${f.reason ? ` — ${f.reason}` : ""}`);
  }

  if (suggestionLines.length > 0) {
    blocks.push({ type: "divider" });
    blocks.push({
      type: "section",
      text: { type: "mrkdwn", text: `*Suggested — needs your OK:*\n${suggestionLines.join("\n")}` },
    });
    blocks.push({
      type: "actions",
      elements: [
        {
          type: "button",
          action_id: CRM_APPLY_ACTION_ID,
          style: "primary" as const,
          text: { type: "plain_text", text: "✅ Apply CRM updates", emoji: true },
          value: p.botId,
        },
      ],
    });
    blocks.push({
      type: "context",
      elements: [{
        type: "mrkdwn",
        text: "Click *Apply* to write these to HubSpot — or reply to change/add fields, stages or properties before applying.",
      }],
    });
  } else {
    blocks.push({
      type: "context",
      elements: [{ type: "mrkdwn", text: "No stage/field changes suggested — the meeting was logged for the record." }],
    });
  }

  return { text, blocks };
}
