// Apply (gated) — invoked by the Slack "Apply CRM updates" button.
// ----------------------------------------------------------------------------

export type CrmApplyResult = { ok: boolean; stageMoved: boolean; fieldsUpdated: number; errors: string[] };

export async function executeCrmProposal(proposal: CrmProposal): Promise<CrmApplyResult> {
  const res: CrmApplyResult = { ok: true, stageMoved: false, fieldsUpdated: 0, errors: [] };

  if (proposal.suggestedStageId) {
    try {
      res.stageMoved = await updateDealStage(proposal.companyId, proposal.dealId, proposal.suggestedStageId);
    } catch (err) {
      res.errors.push(`stage: ${err instanceof Error ? err.message : String(err)}`);
    }
  }

  // Standard DEAL field updates only (company-field writes deferred with #3).
  const dealProps: Record<string, string> = {};
  for (const f of proposal.fieldSuggestions) {
    if (f.object === "deal" && f.property && f.suggestedValue) dealProps[f.property] = f.suggestedValue;
  }
  if (Object.keys(dealProps).length > 0) {
    try {
      const ok = await updateDealProperties(proposal.companyId, proposal.dealId, dealProps);
      if (ok) res.fieldsUpdated = Object.keys(dealProps).length;
      else res.errors.push("field update failed");
    } catch (err) {
      res.errors.push(`fields: ${err instanceof Error ? err.message : String(err)}`);
    }
  }

  res.ok = res.errors.length === 0;
  return res;
}

