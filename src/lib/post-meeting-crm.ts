// ============================================================================
// POST-MEETING CRM SYNC (HubSpot as system of record).
//
// After a meeting, for an ALLOWLISTED company (Luminare only this phase):
//  - AUTO (no approval): log the meeting summary + a direct "Watch recording"
//    link onto the deal/company. Notes/meeting linkage is additive and safe.
//  - SUGGEST (approval): a Slack message proposing the deal STAGE move + any
//    standard field changes, with an "Apply CRM updates" button. Replies can
//    edit it; the button applies through the gated server path (the agent never
//    writes to HubSpot directly).
//
// The suggestion is generated by the shared agent (oneshot, read-only HubSpot),
// which returns stage/field changes in human terms; the SERVER resolves the
// stage label → internal id and performs the gated writes.
// ============================================================================

