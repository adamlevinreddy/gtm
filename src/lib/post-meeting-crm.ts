import { isCompanyWritable } from "@/lib/hubspot-guard";
import {
  logMeetingToHubSpot,
  updateDealStage,
  updateDealProperties,
  getDealPipelines,
  getCompanyContacts,
} from "@/lib/hubspot";

export const CRM_APPLY_ACTION_ID = "pm_crm_apply";
const CRM_TTL = 7 * 24 * 3600;

function recordingUrl(botId: string): string {
  const base = process.env.PUBLIC_BASE_URL ?? "https://gtm-jet.vercel.app";
  return `${base}/board/meeting/${botId}`;
}

export type CrmFieldSuggestion = {
  object: "deal" | "company";
  property: string;
  currentValue: string | null;
  suggestedValue: string;
  reason: string;
};

export type CrmProposal = {
  botId: string;
  companyId: string;
  companyName: string;
  dealId: string;
  dealName: string;
  currentStageLabel: string | null;
  suggestedStageId: string | null;
  suggestedStageLabel: string | null;
  stageReason: string | null;
  fieldSuggestions: CrmFieldSuggestion[];
};

export function crmProposalKey(botId: string): string {
  return `postmeeting:crm:bot:${botId}`;
}

export async function getStoredCrmProposal(botId: string): Promise<CrmProposal | null> {
  if (!botId) return null;
  return (await kv.get<CrmProposal>(crmProposalKey(botId)).catch(() => null)) ?? null;
}

